import { Conversation } from '@/types/chat';
import {
  ExportFormatV1,
  ExportFormatV2,
  ExportFormatV3,
  ExportFormatV4,
  ExportFormatV5,
  LatestExportFormat,
  SupportedExportFormats,
} from '@/types/export';
import { FolderInterface } from '@/types/folder';
import { Prompt } from '@/types/prompt';
import { Tone } from '@/types/tone';

import { cleanConversationHistory } from '../clean';

export function isExportFormatV1(obj: any): obj is ExportFormatV1 {
  return Array.isArray(obj);
}

export function isExportFormatV2(obj: any): obj is ExportFormatV2 {
  return !('version' in obj) && 'folders' in obj && 'history' in obj;
}

export function isExportFormatV3(obj: any): obj is ExportFormatV3 {
  return obj.version === 3;
}

export function isExportFormatV4(obj: any): obj is ExportFormatV4 {
  return obj.version === 4;
}

export function isExportFormatV5(obj: any): obj is ExportFormatV5 {
  return obj.version === 5;
}

export const isLatestExportFormat = isExportFormatV5;

export function cleanData(data: SupportedExportFormats): LatestExportFormat {
  if (isExportFormatV1(data)) {
    return {
      version: 5,
      history: cleanConversationHistory(data),
      folders: [],
      prompts: [],
      tones: [],
      customAgents: [],
    };
  }

  if (isExportFormatV2(data)) {
    return {
      version: 5,
      history: cleanConversationHistory(data.history || []),
      folders: (data.folders || []).map((chatFolder) => ({
        id: chatFolder.id.toString(),
        name: chatFolder.name,
        type: 'chat',
      })),
      prompts: [],
      tones: [],
      customAgents: [],
    };
  }

  if (isExportFormatV3(data)) {
    return {
      version: 5,
      history: cleanConversationHistory(data.history || []),
      folders: data.folders || [],
      prompts: [],
      tones: [],
      customAgents: [],
    };
  }

  if (isExportFormatV4(data)) {
    return {
      version: 5,
      history: cleanConversationHistory(data.history || []),
      folders: data.folders || [],
      prompts: data.prompts || [],
      tones: [],
      customAgents: [],
    };
  }

  if (isExportFormatV5(data)) {
    return data;
  }

  throw new Error('Unsupported data format');
}

function currentDate() {
  const date = new Date();
  const month = date.getMonth() + 1;
  const day = date.getDate();
  return `${month}-${day}`;
}

/**
 * Migrates data from legacy localStorage keys to Zustand storage format.
 * Merges with any existing Zustand data and removes legacy keys after migration.
 * Call this before any export/import operation for backwards compatibility.
 *
 * Legacy keys: conversationHistory, folders, prompts
 * Zustand keys: conversation-storage, settings-storage
 */
function migrateLegacyToZustandStorage(): void {
  // Migrate conversations and folders to conversation-storage
  const legacyHistory = localStorage.getItem('conversationHistory');
  const legacyFolders = localStorage.getItem('folders');

  if (legacyHistory || legacyFolders) {
    const conversationStorage = localStorage.getItem('conversation-storage');
    const existingData = conversationStorage
      ? JSON.parse(conversationStorage)
      : {
          state: {
            conversations: [],
            folders: [],
            selectedConversationId: null,
          },
          version: 1,
        };

    const legacyConversations = legacyHistory ? JSON.parse(legacyHistory) : [];
    const legacyFoldersParsed = legacyFolders ? JSON.parse(legacyFolders) : [];

    // Merge legacy data with existing Zustand data (dedupe by id)
    const mergedConversations = [
      ...(existingData.state?.conversations || []),
      ...legacyConversations,
    ].filter(
      (conv, index, self) => index === self.findIndex((c) => c.id === conv.id),
    );

    const mergedFolders = [
      ...(existingData.state?.folders || []),
      ...legacyFoldersParsed,
    ].filter(
      (folder, index, self) =>
        index === self.findIndex((f) => f.id === folder.id),
    );

    // Write merged data to Zustand storage
    const newConversationData = {
      state: {
        conversations: mergedConversations,
        folders: mergedFolders,
        selectedConversationId:
          existingData.state?.selectedConversationId || null,
      },
      version: 1,
    };
    localStorage.setItem(
      'conversation-storage',
      JSON.stringify(newConversationData),
    );

    // Remove legacy keys after migration
    localStorage.removeItem('conversationHistory');
    localStorage.removeItem('folders');
  }

  // Migrate prompts to settings-storage
  const legacyPrompts = localStorage.getItem('prompts');

  if (legacyPrompts) {
    const settingsStorage = localStorage.getItem('settings-storage');
    const existingSettings = settingsStorage
      ? JSON.parse(settingsStorage)
      : { state: {}, version: 1 };

    const legacyPromptsParsed = JSON.parse(legacyPrompts);

    // Merge legacy prompts with existing
    const mergedPrompts = [
      ...(existingSettings.state?.prompts || []),
      ...legacyPromptsParsed,
    ].filter(
      (prompt, index, self) =>
        index === self.findIndex((p) => p.id === prompt.id),
    );

    existingSettings.state = {
      ...existingSettings.state,
      prompts: mergedPrompts,
    };
    localStorage.setItem('settings-storage', JSON.stringify(existingSettings));

    // Remove legacy key after migration
    localStorage.removeItem('prompts');
  }
}

export const exportData = () => {
  let history = localStorage.getItem('conversationHistory');
  let folders = localStorage.getItem('folders');
  let prompts = localStorage.getItem('prompts');
  let settingsStorage = localStorage.getItem('settings-storage');

  if (history) {
    history = JSON.parse(history);
  }

  if (folders) {
    folders = JSON.parse(folders);
  }

  if (prompts) {
    prompts = JSON.parse(prompts);
  }

  // Extract tones and customAgents from settings-storage
  let tonesArray = [];
  let customAgentsArray = [];
  if (settingsStorage) {
    const settingsData = JSON.parse(settingsStorage);
    tonesArray = settingsData?.state?.tones || [];
    customAgentsArray = settingsData?.state?.customAgents || [];
  }

  const data = {
    version: 5,
    history: history || [],
    folders: folders || [],
    prompts: prompts || [],
    tones: tonesArray,
    customAgents: customAgentsArray,
  } as LatestExportFormat;

  const blob = new Blob([JSON.stringify(data, null, 2)], {
    type: 'application/json',
  });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.download = `chatbot_ui_history_${currentDate()}.json`;
  link.href = url;
  link.style.display = 'none';
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
};

export const importData = (
  data: SupportedExportFormats,
): LatestExportFormat => {
  const { history, folders, prompts, tones, customAgents } = cleanData(data);

  const oldConversations = localStorage.getItem('conversationHistory');
  const oldConversationsParsed = oldConversations
    ? JSON.parse(oldConversations)
    : [];

  const newHistory: Conversation[] = [
    ...oldConversationsParsed,
    ...history,
  ].filter(
    (conversation, index, self) =>
      index === self.findIndex((c) => c.id === conversation.id),
  );
  localStorage.setItem('conversationHistory', JSON.stringify(newHistory));
  if (newHistory.length > 0) {
    localStorage.setItem(
      'selectedConversation',
      JSON.stringify(newHistory[newHistory.length - 1]),
    );
  } else {
    localStorage.removeItem('selectedConversation');
  }

  const oldFolders = localStorage.getItem('folders');
  const oldFoldersParsed = oldFolders ? JSON.parse(oldFolders) : [];
  const newFolders: FolderInterface[] = [
    ...oldFoldersParsed,
    ...folders,
  ].filter(
    (folder, index, self) =>
      index === self.findIndex((f) => f.id === folder.id),
  );
  localStorage.setItem('folders', JSON.stringify(newFolders));

  const oldPrompts = localStorage.getItem('prompts');
  const oldPromptsParsed = oldPrompts ? JSON.parse(oldPrompts) : [];
  const newPrompts: Prompt[] = [...oldPromptsParsed, ...prompts].filter(
    (prompt, index, self) =>
      index === self.findIndex((p) => p.id === prompt.id),
  );
  localStorage.setItem('prompts', JSON.stringify(newPrompts));

  // Handle tones and custom agents - merge with existing settings-storage
  const settingsStorage = localStorage.getItem('settings-storage');
  const settingsData = settingsStorage
    ? JSON.parse(settingsStorage)
    : { state: {} };

  const oldTones = settingsData?.state?.tones || [];
  const newTones = [...oldTones, ...tones].filter(
    (tone, index, self) => index === self.findIndex((t) => t.id === tone.id),
  );

  const oldCustomAgents = settingsData?.state?.customAgents || [];
  const newCustomAgents = [...oldCustomAgents, ...customAgents].filter(
    (agent, index, self) => index === self.findIndex((a) => a.id === agent.id),
  );

  settingsData.state = {
    ...settingsData.state,
    tones: newTones,
    customAgents: newCustomAgents,
  };
  localStorage.setItem('settings-storage', JSON.stringify(settingsData));

  return {
    version: 5,
    history: newHistory,
    folders: newFolders,
    prompts: newPrompts,
    tones: newTones,
    customAgents: newCustomAgents,
  };
};
